export function useI18n() {
  const language = useWorkbenchUiStore((state) => state.language);
  return (key: MessageKey) => {
    const currentLanguage = useWorkbenchUiStore.getState().language || language;
    return messages[currentLanguage][key] || messages["en-US"][key] || key;
  };
}
